package Video2;
import java.util.*;

public class adding {
    
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sum = a + b;
        int diff = b - a;
        int mul = a * b;
        System.out.println(sum);
        System.out.println(diff);
        System.out.println(mul);
    }
    
}
