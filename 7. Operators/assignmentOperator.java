public class assignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        a+= 5;

        int b = 15;
        b-=5;
        System.out.println(b);
        
        b*=5;
        System.out.println(b);
        b/=5;
        System.out.println(b);
        b%=5;
        
        System.out.println(b);
    }
    
}
